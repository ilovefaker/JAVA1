package cn.zucc.day05;

public class Penguin {

	String name;
	String cloths;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCloths() {
		return cloths;
	}
	public void setCloths(String cloths) {
		this.cloths = cloths;
	}
	public Penguin(String name, String cloths) {
		super();
		this.name = name;
		this.cloths = cloths;
	}
	public Penguin() {
		super();
	}
	
	
	
	
	
}
